-- =============================================================================
-- WorkBuddy 数据库管理脚本：数据清理与 VACUUM
-- 描述: 定期执行的数据生命周期管理，控制存储膨胀
-- 使用方式: 由应用内的定时任务调度器每日凌晨调用
-- =============================================================================

-- -----------------------------------------------------------------------
-- 步骤 1: 清理超过 N 天的截图文件路径记录
-- （截图文件本身由 Rust 层负责物理删除，此处仅清理数据库记录）
-- -----------------------------------------------------------------------
DELETE FROM captures
WHERE
    screenshot_path IS NOT NULL
    AND ts < (CAST(strftime('%s', 'now') AS INTEGER) - (
        CAST((SELECT value FROM user_preferences WHERE key = 'privacy.screenshot_keep_days') AS INTEGER) * 86400
    )) * 1000;

-- -----------------------------------------------------------------------
-- 步骤 2: 清理超过 180 天的全量 captures 记录（仅保留文本摘要）
-- 超过半年的历史数据价值有限，进行整体清理
-- -----------------------------------------------------------------------
DELETE FROM captures
WHERE ts < (CAST(strftime('%s', 'now') AS INTEGER) - 180 * 86400) * 1000;

-- -----------------------------------------------------------------------
-- 步骤 3: 清理孤立的 vector_index 记录
-- （captures 被删除后，级联 DELETE 应已处理，此处作双重保障）
-- -----------------------------------------------------------------------
DELETE FROM vector_index
WHERE capture_id NOT IN (SELECT id FROM captures);

-- -----------------------------------------------------------------------
-- 步骤 4: 低质量样本清理（quality < 0.2 且超过 30 天）
-- -----------------------------------------------------------------------
DELETE FROM style_samples
WHERE
    quality < 0.2
    AND ts < (CAST(strftime('%s', 'now') AS INTEGER) - 30 * 86400) * 1000;

-- -----------------------------------------------------------------------
-- 步骤 5: 重建 FTS5 索引（防止碎片化导致检索变慢）
-- -----------------------------------------------------------------------
INSERT INTO captures_fts(captures_fts) VALUES('rebuild');

-- -----------------------------------------------------------------------
-- 步骤 6: SQLite VACUUM（整理数据库文件，回收已删除记录占用的空间）
-- 注意: VACUUM 不能在事务内执行，由应用层单独调用
-- -----------------------------------------------------------------------
-- VACUUM;   -- 此行由 Rust 应用代码在事务外单独执行

-- -----------------------------------------------------------------------
-- 记录本次清理日志
-- -----------------------------------------------------------------------
INSERT INTO data_cleanup_log (ts, cleanup_type, affected_count, freed_bytes, detail)
VALUES (
    CAST(strftime('%s', 'now') * 1000 AS INTEGER),
    'scheduled_cleanup',
    0,      -- Rust 层在执行后填入实际影响行数
    0,      -- Rust 层在 VACUUM 后填入实际释放字节数
    'Daily scheduled cleanup'
);
